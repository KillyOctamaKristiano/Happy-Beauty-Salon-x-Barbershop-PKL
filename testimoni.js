// Fungsi utama menampilkan halaman
function showPage(pageNumber) {
  frame18.innerHTML = '';

  pages[pageNumber].forEach((src, index) => {
    const img = document.createElement('img');
    img.src = src;
    img.className = 'IMG-' + (index + 1);
    img.onerror = () => console.warn('Gagal dimuat:', src);
    frame18.appendChild(img);
  });

  fixImageSizes();

  buttons.forEach(btn => btn.classList.remove('active'));
  buttons[pageNumber - 1].classList.add('active');
}

